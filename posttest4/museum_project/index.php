          <h3>Traditional Arts and Creative Identities</h3>
          <p>
            From a fourth-generation hoop net maker to a Zapotec weaver, this
            exhibition explores how makers from diverse communities throughout
            Indiana assert their personal and cultural identities through
            traditional arts. While nets, quilts, paperweights, weavings, and
            banjo tunes are creative expressions, they also communicate a
            maker’s sense of self and community. Each artist in the exhibition
            is a member of Traditional Arts Indiana’s Apprenticeship Program.
            Their work includes art forms long practiced in Indiana as well as
            traditions new to the state. This exhibition tells the story of how
            these artists are teaching their traditions to the next generation,
            which is perhaps the most complete expression of one’s cultural
            identity.
          </p>
